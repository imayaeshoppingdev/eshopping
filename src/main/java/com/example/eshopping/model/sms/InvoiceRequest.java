package com.example.eshopping.model.sms;

public class InvoiceRequest {

	private String md;
	private String cgm;
	private String o_pin;
	private String ss;
	private String d_pin;
	
	
	public String getMd() {
		return md;
	}
	public void setMd(String md) {
		this.md = md;
	}
	public String getCgm() {
		return cgm;
	}
	public void setCgm(String cgm) {
		this.cgm = cgm;
	}
	public String getO_pin() {
		return o_pin;
	}
	public void setO_pin(String o_pin) {
		this.o_pin = o_pin;
	}
	public String getSs() {
		return ss;
	}
	public void setSs(String ss) {
		this.ss = ss;
	}
	public String getD_pin() {
		return d_pin;
	}
	public void setD_pin(String d_pin) {
		this.d_pin = d_pin;
	}
	
	
	
}
