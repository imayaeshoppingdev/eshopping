package com.example.eshopping.model.sms;

public class UpdateWarehouse {

	private String name;
	private String registered_name;
	private String address;
	private String pin;
	private String phone;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRegistered_name() {
		return registered_name;
	}
	public void setRegistered_name(String registered_name) {
		this.registered_name = registered_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPin() {
		return pin;
	}
	public void setPin(String pin) {
		this.pin = pin;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
	
}
