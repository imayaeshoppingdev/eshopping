package com.example.eshopping.model.product;

public class Suggesion {

	private String id;
	private String suggesion;
	private String categoryId;
	private String subCategoryId;
	private String productListingId;
	private String suggesionType;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSuggesion() {
		return suggesion;
	}
	public void setSuggesion(String suggesion) {
		this.suggesion = suggesion;
	}
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getSubCategoryId() {
		return subCategoryId;
	}
	public void setSubCategoryId(String subCategoryId) {
		this.subCategoryId = subCategoryId;
	}
	public String getProductListingId() {
		return productListingId;
	}
	public void setProductListingId(String productListingId) {
		this.productListingId = productListingId;
	}
	public String getSuggesionType() {
		return suggesionType;
	}
	public void setSuggesionType(String suggesionType) {
		this.suggesionType = suggesionType;
	}
	
	
	
}
